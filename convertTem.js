
let originalCelsius = 25; 

let fahrenheit = (originalCelsius * 9/5) + 32;

let convertedCelsius = (fahrenheit - 32) * 5/9;


console.log("Original Celsius Temperature: " + originalCelsius + " degree Celsius");
console.log("Converted to Fahrenheit: " + fahrenheit + " degree fahrenheit");
console.log("Converted back to Celsius: " + convertedCelsius + " degree Celsius");
